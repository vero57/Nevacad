buah = ['apel', 'pisang', 'semangka']

# Menambahkan jeruk ke index terakhir
buah.append('jeruk')

# menambahkan melon ke index ke2
buah.insert(2, "melon")

# Mengubah index ke 0 menjadi nanas
buah[0] = 'nanas'

# menghapus pisang dari list
buah.remove('pisang')

# menghapus data terakhir
buah.pop()

print(buah)